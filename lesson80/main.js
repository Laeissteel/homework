// Вывел в консоль четные числа от 2 до 12, используя цикл for и условие.

for(let i = 2; i <= 12; i = i + 2) {
  console.log(`${i}`)
}

// При помощи цикла for вывел в консоль числа от 10 до 1, в обратном порядке.

for(let i = 10; i >= 1; i = i - 1) {
  console.log(`${i}`)
}

// Переписал 1 и 2 цикл используя цикл while

let i = 0 

while(i < 12) {
  i = i + 2
  console.log(`${i}`)
}

